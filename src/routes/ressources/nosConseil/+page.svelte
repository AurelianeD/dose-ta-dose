    <h1><span class="encadre">Nos conseils</span> et alcool</h1>
    <div class="paragraphe">
      <p>Boire de l'alcool peut parfois ressembler à un passage obligé pour "être cool" ou se sentir intégré, surtout au lycée ou au collège. Mais d'où vient cette pression ? Et pourquoi est-ce un piège à absolument éviter ? Une bière entre deux heures de cours, un apéro avec la famille, une coupe de champagne lors d'un mariage... L'alcool nous entoure, et nous sommes constamment confrontés à sa consommation dès notre plus jeune âge. <br><br> Cela tombe bien, car nous allons justement aborder ce sujet plus en profondeur. L'idée est que tu puisses, grâce à nos conseils, mieux résister à la pression des pairs mais aussi comprendre d'où peut venir cette influence.</p>
    </div>
    <div class="paragraphe">
        <h2>Les amies et la famille d’abord, pas toujours… </h2>
        <p>C’est vendredi soir, tout le monde est excité à l’idée de finir la semaine en beauté. Les amis te passent une bouteille en te disant : “Allez juste un verre!”, c’est là que commence la pression des pairs. En gros, c’est quand tes amis (ou parfois même des connaissances) te poussent à boire juste parce que “tout le monde le fait”. Mais la pression ne vient pas uniquement des amis. Dans la famille aussi, lors d’événements comme les mariages ou les fêtes, l’alcool est souvent proposé et les attitudes permissives ou restrictives de ta famille peuvent également influencer ta consommation d'alcool. Les parents et les frères et sœurs aînés peuvent servir de modèle, que ce soit pour boire modérément ou au contraire, excéder les limites. Dans ce genre de situation, il est difficile de s’affirmer et de dire non, car en tant que collégien ou lycéen, tu veux être apprécié et faire partie du groupe. Mais il est crucial que tu gardes à l’esprit que tu as le droit de dire non, et que cela peut te sauver de personnes potentiellement malveillantes et de situations dangereuses. Ne t’inquiète pas, par la suite nous te donnerons des conseils pour t’affirmer et enfin pouvoir dire non sans aucune difficulté, tu verras c’est juste une question d’entraînement!</p>
    </div>
    <div class="paragraphe">
        <h2>La publicité et les médias qui vend du rêve… et de l’alcool</h2>
        <p>Allume la télé, regarde une vidéo sur Youtube, et paf une publicité pour une boisson alcoolisée apparaît montrant des gens heureux, beaux passant le plus beau jour de leur vie, un verre d’alcool fort à la main.  Les publicités et les médias jouent un rôle crucial dans ta perception de l'alcool, te vendant l'idée que boire est synonyme d'être "cool" et "populaire". <br> Mais attention, ne te fait pas voir, cette image est trompeuse. En réalité l’alcool agit directement sur le bon fonctionnement de ton cerveau. Des difficultés à se concentrer, changement brusque de l’humeur, hypertension, battements du cœur qui fluctuent, bref tu l’auras compris c’est tout sauf cool que de consommer de l’alcool aussi tôt.</p>
    </div>
    <div class="paragraphe">
        <h2>Nos conseil</h2>
        <p>Il est important d’apprendre à savoir dire “non” même si sur le moment cela peut sembler difficile. <br><br> Cependant, s’affirmer implique également d'oser exprimer ses opinions et ses préférences, et de prendre ses propres décisions en restant fidèle à soi-même. Dire non est une compétence qui se développe avec le temps, jour après jour. Pour y parvenir, tu peux :</p>
        <ul>
          <li>Trouver ton partenaire dans ton entourage qui partage tes valeurs.</li>
          <li>Développer ta confiance en soi, et exprimer ses émotions. (des ateliers existent, comme le théâtre par exemple)</li>
          <li>Propose une alternative, si tu ne veux pas boire alcool tu peux prendre des softs, des jus..</li>
          <li>Ne pas te sentir coupable </li>
        </ul>
        <p>Si tu remarques dans ton entourage un comportement qui t’inquiète, il ne faut surtout pas que tu hésites à lui venir en aide, pour cela tu peux :</p>
        <ul>
          <li><span class="bold">Exprime lui ton soutien :</span> Dis-lui que tu t’inquiètes pour sa santé et son bien-être, et que tu es là pour l'aider.</li>
          <li><span class="bold">Dirige le/la vers des professionnels :</span> Conseille lui de prendre contact avec des personnes professionnelles qui pourront lui apporter une véritable aide.</li>
          <li><span class="bold">Évite la consommation d’alcool : </span>Pendant les soirées ou les sorties, évite de l’inciter à boire, même juste un verre ! Propose lui plutôt d’autres alternatives d’une boisson alcoolisée.</li>
          <li><span class="bold">Fais des activités :</span> Tu peux lui proposer de sortir et de faire des activités alternatives qui n'impliquent pas la consommation d'alcool, comme faire du sport, se promener ou d’autres activités ludiques.</li>
        </ul>
